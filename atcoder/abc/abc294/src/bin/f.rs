#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    let m: usize = sc.cin();
    let k: usize = sc.cin();

    let xs: Vec<(f64, f64)> = (0..n).map(|_| (sc.cin(), sc.cin())).collect();
    let ys: Vec<(f64, f64)> = (0..m).map(|_| (sc.cin(), sc.cin())).collect();

    let mut left = 0.0; // これ以上が k 本以上ある
    let mut right = 1.1; // これ以上が k 本未満である
    for _ in 0..100 {
        let mid = (left + right) / 2.0;
        if num_geq(mid, &xs, &ys) >= k {
            left = mid;
        } else {
            right = mid;
        }
    }
    put!(left * 100.0);
}

fn num_geq(theta: f64, xs: &Vec<(f64, f64)>, ys: &Vec<(f64, f64)>) -> usize {
    let xs: Vec<f64> = xs
        .iter()
        .map(|&(a, b)| (1.0 - theta) * a - theta * b)
        .collect();
    let mut ys: Vec<Float> = ys
        .iter()
        .map(|&(a, b)| (1.0 - theta) * a - theta * b)
        .map(|x| Float(x))
        .collect();
    let mut num = BTreeMap::new();
    {
        ys.sort();
        let m = ys.len();
        for i in 0..m {
            num.insert(ys[i], m - i);
        }
    }
    trace!(&num);
    let mut k = 0;
    for x in xs {
        trace!(x, Float(-x));
        if let Some((_, n)) = num.range(Float(-x)..).min() {
            k += n;
        }
    }
    k
}

