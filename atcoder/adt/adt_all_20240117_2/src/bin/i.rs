#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let s: usize = sc.cin();
    let t: usize = sc.cin();
    let m: usize = sc.cin();

    let mut g = vec![vec![]; s];

    for _ in 0..m {
        let u = sc.usize1();
        let v = sc.usize1();
        let v = v - s;
        g[u].push(v);
    }

    let mut pairs: BTreeMap<(usize, usize), usize> = BTreeMap::new();
    for u in 0..s {
        g[u].sort();
        for &v1 in g[u].iter() {
            for &v2 in g[u].iter() {
                if v1 >= v2 {
                    continue;
                }
                if let Some(u1) = pairs.get(&(v1, v2)) {
                    put!(u1 + 1, u + 1, s + v1 + 1, s + v2 + 1);
                    return;
                }
                pairs.insert((v1, v2), u);
            }
        }
    }
    put!(-1);
}

