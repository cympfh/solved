#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let a: f64 = sc.cin();
    let b: f64 = sc.cin();
    let mut left = 0.0;
    let mut right = a;
    for _ in 0..1000 {
        let z1 = (left + left + right) / 3.0;
        let z2 = (left + right + right) / 3.0;
        let t1 = a / (1.0 + z1).sqrt() + b * z1;
        let t2 = a / (1.0 + z2).sqrt() + b * z2;
        trace!((z1, z2), (t1, t2));
        if t1 > t2 {
            left = z1;
        } else {
            right = z2;
        }
    }
    let mut ans = a;
    let left = max!(0, left.trunc() as i64 - 10);
    let right = right.trunc() as i64 + 10;
    for k in left..=right {
        let z = k as f64;
        let t = a / (1.0 + z).sqrt() + b * z;
        ans = min!(ans, t);
    }
    put!(ans);
}

