#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn ord(a: char) -> usize {
    (a as u8 - 'a' as u8) as usize
}

fn main() {
    let mut sc = Scanner::new();
    let n: usize = sc.cin();
    let k: usize = sc.cin();
    let ss: Vec<HashSet<usize>> = (0..n)
        .map(|_| sc.chars().into_iter().map(ord).collect())
        .collect();

    let mut ans = 0;
    for iset in 0..(1 << n) {
        let mut cx = 0;
        for alpha in 0..26 {
            let mut cnt = 0;
            for i in 0..n {
                if iset & (1 << i) == 0 {
                    continue;
                }
                if ss[i].contains(&alpha) {
                    cnt += 1;
                }
            }
            if cnt == k {
                cx += 1;
            }
        }
        ans = max!(ans, cx);
    }
    put!(ans);
}

