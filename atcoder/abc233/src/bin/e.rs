#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::new();
    let digits: Vec<char> = sc.chars();
    let m = digits.len();

    let mut ds = vec![0; m * 2];
    for i in 0..m {
        ds[m + i] = ((digits[i] as u8) - ('0' as u8)) as u64;
    }

    trace!(&ds);
    for i in 1..m * 2 {
        ds[i] += ds[i - 1];
    }
    for i in (1..m * 2).rev() {
        let q = ds[i] / 10;
        ds[i] %= 10;
        ds[i - 1] += q;
    }
    let mut zero = true;
    for &d in ds.iter() {
        if zero && d == 0 {
            continue;
        }
        zero = false;
        print!("{}", d);
    }
    if zero {
        print!("0");
    }
    println!();
}

