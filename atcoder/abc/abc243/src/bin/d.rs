use std::{cmp::*, collections::*};

fn shorten(cmd: &Vec<char>) -> Vec<char> {
    let mut ret = vec![];
    for &c in cmd.iter() {
        if c == 'U' && ret.len() > 0 && ret[ret.len() - 1] != 'U' {
            let _ = ret.pop();
        } else {
            ret.push(c);
        }
    }
    ret
}

fn main() {
    let mut sc = Scanner::new();
    let _n: usize = sc.cin();
    let mut x: u128 = sc.cin();
    let cmd = sc.chars();
    trace!(&cmd);
    let cmd = shorten(&cmd);
    trace!(&cmd);

    for &c in cmd.iter() {
        if c == 'U' {
            x /= 2;
        } else if c == 'L' {
            x *= 2;
        } else {
            x = x * 2 + 1;
        }
    }
    put!(x);
}

