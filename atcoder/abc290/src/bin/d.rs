#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    for _ in 0..n {
        let n: usize = sc.cin();
        let d: usize = sc.cin();
        let k: usize = sc.cin();
        let d = d % n;

        // trivial case
        if k == 1 {
            put!(0);
            continue;
        }
        if d == 1 {
            put!(k - 1);
            continue;
        }
        if d == 0 {
            put!(k - 1);
            continue;
        }

        // d > 1 && k > 1
        if n % d == 0 {
            let m = n / d; // shuki
            let ans = (k - 1) / m + ((k - 1) % m) * d;
            put!(ans);
            continue;
        }

        if gcd(n, d) > 1 {
            let g = gcd(n, d);
            let m = n / g;
            let mut ans = (k - 1) / m + ((k - 1) % m) * d;
            ans %= n;
            put!(ans);
            continue;
        }

        let ans = ((k - 1) * d) % n;
        put!(ans);
    }
}

// @num/gcd
/// Number - GCD on Natural Numbers
pub fn gcd(a: usize, b: usize) -> usize {
    if b == 0 {
        a
    } else {
        gcd(b, a % b)
    }
}

