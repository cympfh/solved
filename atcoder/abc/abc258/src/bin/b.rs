#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    let mat: Vec<Vec<i64>> = (0..n)
        .map(|_| {
            sc.chars()
                .into_iter()
                .map(|c| ((c as u8) - ('0' as u8)) as i64)
                .collect()
        })
        .collect();
    let dx = [0, 1, 1, 1, 0, -1, -1, -1];
    let dy = [1, 1, 0, -1, -1, -1, 0, 1];
    let mut ans = vec![];
    for i in 0..n {
        for j in 0..n {
            for d in 0..8 {
                let mut ds = vec![];
                for k in 0..n {
                    let i = ((i as i64 + dx[d] * k as i64 + 100 * n as i64) as usize) % n;
                    let j = ((j as i64 + dy[d] * k as i64 + 100 * n as i64) as usize) % n;
                    ds.push(mat[i][j]);
                }
                ans.push(ds);
            }
        }
    }
    ans.sort();
    ans.reverse();
    put!(ans[0].iter().map(|c| format!("{}", c)).collect::<String>());
}

