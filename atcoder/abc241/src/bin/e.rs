#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::new();
    let n: usize = sc.cin();
    let n_u64 = n as u64;
    let mut k: u64 = sc.cin();
    let a: Vec<u64> = sc.vec(n);

    let mut x: u64 = 0;

    for _ in 0..300_000 {
        if k == 0 {
            put!(x);
            return;
        }
        k -= 1;
        x += a[(x % n_u64) as usize];
    }

    let mut shuki = 1;
    let mut diff = 0;
    {
        let mut x = x;
        let a0 = a[(x % n_u64) as usize];
        let state0 = (x % n_u64, a0);
        diff += a0;
        trace!(state0);
        x += a0;
        for _ in 0.. {
            let a1 = a[(x % n_u64) as usize];
            let state = (x % n_u64, a1);
            trace!(state);
            if state0 == state {
                break;
            }
            x += a1;
            diff += a1;
            shuki += 1;
        }
    }
    trace!(shuki, diff);

    let times = k / shuki;
    x += diff * times;
    k -= shuki * times;

    while k > 0 {
        k -= 1;
        x += a[(x % n_u64) as usize];
    }

    put!(x);
}

