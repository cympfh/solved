#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

#[derive(Debug)]
struct Array {
    x: u32,
    y: u32,
    z: u32,
    w: u32,
}
impl Array {
    fn new(x: u32) -> Self {
        let y = 1;
        let z = 2;
        let w = 3;
        Array { x, y, z, w }
    }
    fn next(&mut self) -> u32 {
        let t = self.x ^ (self.x << 11);
        self.x = self.y;
        self.y = self.z;
        self.z = self.w;
        self.w = (self.w ^ (self.w >> 19)) ^ (t ^ (t >> 8));
        self.w
    }
}

fn main() {
    let mut sc = Scanner::new();
    let seed: u32 = sc.cin();
    const LENGTH: usize = 1000_0001;
    let mut arr = Array::new(seed);
    let mut xs = vec![];
    for _ in 0..LENGTH {
        xs.push(arr.next());
        if xs.len() > LENGTH / 2 {
            xs.sort();
            let m = xs.len() / 4;
            xs = xs[m..xs.len() - m].to_vec();
        }
    }
    xs.sort();
    put!(xs[xs.len() / 2]);
}

