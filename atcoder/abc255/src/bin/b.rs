#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn dist(p: (f64, f64), q: (f64, f64)) -> f64 {
    (p.0 - q.0).powi(2) + (p.1 - q.1).powi(2)
}

fn main() {
    let mut sc = Scanner::new();
    let n: usize = sc.cin();
    let k: usize = sc.cin();
    let a: Vec<usize> = sc.vec::<usize>(k).into_iter().map(|i| i - 1).collect();
    let ps: Vec<(f64, f64)> = (0..n)
        .map(|_| {
            let x: f64 = sc.cin();
            let y: f64 = sc.cin();
            (x, y)
        })
        .collect();
    let mut left = 0.0; // dont pass
    let mut right = 2_000_000.0; // can pass
    for _ in 0..200 {
        let mid = (left + right) / 2.0;
        let mut lighten = vec![false; n];
        for &i in a.iter() {
            for j in 0..n {
                if dist(ps[i], ps[j]) < mid * mid {
                    lighten[j] = true;
                }
            }
        }
        if lighten.iter().all(|&z| z) {
            right = mid;
        } else {
            left = mid;
        }
    }
    put!(right);
}

