#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    let mut persons = vec![];
    for i in 0..n {
        let a: i64 = sc.cin();
        let b: i64 = sc.cin();
        persons.push((a, b, i + 1));
    }
    persons.sort_by(|p, q| {
        let a = p.0 * q.1;
        let b = p.1 * q.0;
        if a < b {
            Ordering::Greater
        } else if a > b {
            Ordering::Less
        } else {
            p.2.cmp(&q.2)
        }
    });
    trace!(persons);
    let ans: Vec<usize> = persons.into_iter().map(|p| p.2).collect();
    put!(..ans);
}

