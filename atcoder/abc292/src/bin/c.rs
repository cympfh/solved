#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

// x = a * b なる (a,b)
fn f(x: u64) -> u64 {
    let mut r = 0;
    for a in 1..=x {
        if a * a > x {
            break;
        }
        if x % a != 0 {
            continue;
        }
        if a * a == x {
            r += 1;
            break;
        }
        r += 2;
    }
    r
}

fn main() {
    let mut sc = Scanner::default();
    let n: u64 = sc.cin();
    let mut ans = 0;
    for x in 1..n {
        let y = n - x;
        ans += f(x) * f(y);
    }
    put!(ans);
}

