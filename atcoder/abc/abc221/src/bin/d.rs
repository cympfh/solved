#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::new();
    let n: usize = sc.cin();
    let mut evs = DefaultDict::new(0_i64);
    let mut ans = vec![0; n + 1];
    for _ in 0..n {
        let a: usize = sc.cin();
        let b: usize = sc.cin();
        evs[a] += 1;
        evs[a + b] -= 1;
    }
    trace!(&evs);
    let mut p = 0_i64;
    let mut last = 0;
    for (&z, &dp) in evs.iter() {
        trace!((p, last), (z, dp));
        ans[p as usize] += z - last;
        p += dp;
        last = z
    }
    let ans = ans[1..].to_vec();
    put!(..ans);
}

