#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

type P = (f64, f64);

fn diff(x: P, y: P) -> P {
    (x.0 - y.0, x.1 - y.1)
}
fn norm(x: P) -> f64 {
    (x.0.powi(2) + x.1.powi(2)).sqrt()
}

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    let k: usize = sc.cin();
    let s: (f64, f64) = (sc.cin(), sc.cin());
    let ps: Vec<(f64, f64)> = (0..n).map(|_| (sc.cin(), sc.cin())).collect();

    let ps: Vec<P> = ps.into_iter().map(|(x, y)| (x - s.0, y - s.1)).collect();

    // dp[i][k] = 子供 i をちょうど訪れ終わった and (残りのプレゼント + 訪れた頂点数) が k,
    // という状態での最小距離
    let mut dp: LazySegmentTree<MinInt<Float>, M> = LazySegmentTree::new(n + k + 10);
    dp.update(k..k + 1, M::Update(Float(0.0)));

    for i in 1..n {
        trace!(dp.product(i..k + i));
        let min = dp.product(i..k + i).unwrap();
        dp.update(i..k + i, M::Add(Float(norm(diff(ps[i], ps[i - 1])))));
        dp.update(
            k + i..k + i + 1,
            M::Update(Float(min.0 + norm(ps[i - 1]) + norm(ps[i]))),
        );
        // trace!(dp.to_vec());
    }
    trace!(dp.product(n..k + n + 1));
    put!(dp.product(n..k + n + 1).unwrap().0 + norm(ps[0]) + norm(ps[n - 1]));
}

