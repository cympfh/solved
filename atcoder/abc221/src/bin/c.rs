#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn ord(c: char) -> u32 {
    c as u32 - '0' as u32
}

fn toint(x: &Vec<u32>) -> u32 {
    let mut r = 0;
    for &digit in x.iter().rev() {
        r = r * 10 + digit;
    }
    r
}

fn main() {
    let mut sc = Scanner::new();
    let n = sc.chars();
    let len = n.len();
    let mut ans = 0;
    for iset in 0..(1 << len) {
        let mut a = vec![];
        let mut b = vec![];
        for i in 0..len {
            let x = ord(n[i]);
            if (iset >> i) & 1 > 0 {
                a.push(x);
            } else {
                b.push(x);
            }
        }
        a.sort();
        b.sort();
        let a = toint(&a);
        let b = toint(&b);
        ans = max!(ans, a * b);
    }
    put!(ans);
}

