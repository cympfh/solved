use std::{cmp::*, collections::*};

fn is_prefix(s: String, t: String) -> bool {
    match t.match_indices(&s).next() {
        Some((0, _)) => true,
        _ => false,
    }
}

fn is_suffix(s: String, t: String) -> bool {
    let s: String = s.chars().rev().collect();
    let t: String = t.chars().rev().collect();
    match t.match_indices(&s).next() {
        Some((0, _)) => true,
        _ => false,
    }
}

fn main() {
    let mut sc = Scanner::default();
    let _: usize = sc.cin();
    let _: usize = sc.cin();
    let s: String = sc.cin();
    let t: String = sc.cin();
    let ans = match (is_prefix(s.clone(), t.clone()), is_suffix(s, t)) {
        (true, true) => 0,
        (true, false) => 1,
        (false, true) => 2,
        (false, false) => 3,
    };
    put!(ans);
}

