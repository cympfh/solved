#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::new();
    let x1: i64 = sc.cin();
    let y1: i64 = sc.cin();
    let x2: i64 = sc.cin();
    let y2: i64 = sc.cin();
    for x in x1 - 10..x1 + 10 {
        for y in y1 - 10..y1 + 10 {
            if d2(x, y, x1, y1) == 5 && d2(x, y, x2, y2) == 5 {
                put!("Yes");
                return;
            }
        }
    }
    put!("No");
}

fn d2(x: i64, y: i64, x2: i64, y2: i64) -> i64 {
    (x - x2).pow(2) + (y - y2).pow(2)
}

