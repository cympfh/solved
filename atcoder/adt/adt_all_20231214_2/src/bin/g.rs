/// Number - Prime Factorization
pub fn factorize(n: u64) -> Vec<(u64, usize)> {
    let mut m = n;
    let mut r = vec![];
    for x in 2..=n {
        if m == 1 {
            break;
        }
        if x * x > n {
            r.push((m, 1));
            break;
        }
        if n % x != 0 {
            continue;
        }
        let mut c = 0;
        while m % x == 0 {
            m /= x;
            c += 1;
        }
        if c > 0 {
            r.push((x, c));
        }
    }
    r
}

fn main() {
    let mut sc = Scanner::default();
    let n: u64 = sc.cin();

    let mut sqs = vec![];
    for q in 1.. {
        if q * q > n {
            break;
        }
        sqs.push(q * q);
    }

    let mut ans = 0;
    for x in 1..=n {
        let xf = factorize(x);
        let mut y = 1;
        for (p, c) in xf {
            if c % 2 == 1 {
                y *= p;
            }
        }
        for &sq in sqs.iter() {
            if y * sq <= n {
                ans += 1;
            } else {
                break;
            }
        }
    }
    put!(ans);
}

