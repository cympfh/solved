#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::new();
    let n: usize = sc.cin();
    let is_adult: Vec<bool> = sc.chars().into_iter().map(|c| c == '1').collect();
    let ws: Vec<i64> = sc.vec(n);

    let mut children = vec![];
    let mut adults = vec![];
    for i in 0..n {
        if is_adult[i] {
            adults.push(ws[i]);
        } else {
            children.push(ws[i]);
        }
    }
    children.sort();
    adults.sort();

    let mut cans = ws.clone();
    cans.push(0);
    cans.push(1_000_000_010);

    let mut ans = 0;
    for w in cans {
        let a1 = count(&children, 0..w);
        let a2 = count(&adults, w..1_000_000_010);
        let acc = a1 + a2;
        ans = max!(ans, acc);
    }
    put!(ans);
}

