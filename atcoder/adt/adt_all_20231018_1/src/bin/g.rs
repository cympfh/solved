#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    let a: Vec<i64> = sc.vec(n);

    // (left, right, length)
    let mut spans: Vec<(i64, i64, i64)> = vec![];
    for i in 1..n {
        if i % 2 == 0 {
            let left = a[i - 1];
            let right = a[i];
            spans.push((left, right, right - left));
        }
    }
    trace!(&spans);

    // memo[x] = [0, x) で何分寝た?
    let mut memo = BTreeMap::new();
    {
        let mut acc = 0;
        memo.insert(0, 0);
        for &(left, right, length) in spans.iter() {
            memo.insert(left, acc);
            acc += length;
            memo.insert(right, acc);
        }
    }
    trace!(&memo);

    let spans: BTreeMap<i64, _> = spans
        .into_iter()
        .map(|(left, right, length)| (left, (left, right, length)))
        .collect();
    trace!(&spans);

    let sumlength = |x: i64| -> i64 {
        trace!(x, spans.range(..=x).next_back());
        match spans.range(..=x).next_back() {
            Some((_, &(left, right, _))) => {
                if left <= x && x <= right {
                    memo[&left] + x - left
                } else {
                    memo[&right]
                }
            }
            _ => 0,
        }
    };

    let q: usize = sc.cin();
    for _ in 0..q {
        let left: i64 = sc.cin();
        let right: i64 = sc.cin();
        let ans = sumlength(right) - sumlength(left);
        put!(ans);
    }
}

