#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

#[derive(Debug, Clone, Default)]
struct Array {
    sorted: BinaryHeap<Reverse<u64>>,
    unsorted: VecDeque<u64>,
}

impl Array {
    fn push(&mut self, item: u64) {
        self.unsorted.push_back(item);
    }
    fn sort(&mut self) {
        while let Some(u) = self.unsorted.pop_front() {
            self.sorted.push(Reverse(u));
        }
    }
    fn pop(&mut self) -> Option<u64> {
        if let Some(Reverse(u)) = self.sorted.pop() {
            return Some(u);
        }
        if let Some(u) = self.unsorted.pop_front() {
            return Some(u);
        }
        None
    }
}

fn main() {
    let mut sc = Scanner::new();
    let mut arr = Array::default();
    let n: usize = sc.cin();
    for _ in 0..n {
        let ty: usize = sc.cin();
        if ty == 1 {
            let x: u64 = sc.cin();
            arr.push(x);
        } else if ty == 2 {
            put!(arr.pop().unwrap());
        } else {
            arr.sort();
        }
    }
}

