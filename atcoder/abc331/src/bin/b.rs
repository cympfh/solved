#![allow(unused_imports, unused_macros, dead_code)]
use std::{cmp::*, collections::*};

fn main() {
    let mut sc = Scanner::default();
    let n: usize = sc.cin();
    let s: u64 = sc.cin();
    let m: u64 = sc.cin();
    let l: u64 = sc.cin();
    let mut dp = vec![999_999_999_999_u64; 200];
    dp[0] = 0;
    for x in 0..110 {
        dp[x + 6] = min!(dp[x + 6], dp[x] + s);
    }
    for x in 0..110 {
        dp[x + 8] = min!(dp[x + 8], dp[x] + m);
    }
    for x in 0..110 {
        dp[x + 12] = min!(dp[x + 12], dp[x] + l);
    }
    put!((n..200).map(|i| dp[i]).min().unwrap());
}

